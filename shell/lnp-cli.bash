    local i cur prev opts cmds
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${i}" in
            "$1")
                cmd="lnp__cli"
                ;;
            channels)
                cmd+="__channels"
                ;;
            connect)
                cmd+="__connect"
                ;;
            funds)
                cmd+="__funds"
                ;;
            help)
                cmd+="__help"
                ;;
            info)
                cmd+="__info"
                ;;
            invoice)
                cmd+="__invoice"
                ;;
            listen)
                cmd+="__listen"
                ;;
            open)
                cmd+="__open"
                ;;
            pay)
                cmd+="__pay"
                ;;
            peers)
                cmd+="__peers"
                ;;
            ping)
                cmd+="__ping"
                ;;
            *)
                ;;
        esac
    done

    case "${cmd}" in
        lnp__cli)
            opts="-h -V -R -v --help --version --connect --verbose listen connect ping info funds peers channels open invoice pay help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 1 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                --connect)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                -R)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
